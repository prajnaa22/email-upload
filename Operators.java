package sample;

public class Operators {

	public static void main(String[] args) {
		int x,y,ans;
		x=10;
		y=20;
		ans=x+y;
		System.out.println("print answer x+y="+ans);
		ans=x-y;
		System.out.println("print answer x-y="+ans);
		ans=x*y;
		System.out.println("print answer x*y="+ans);
	}

}
